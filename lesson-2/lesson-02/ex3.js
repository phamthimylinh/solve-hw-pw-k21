// Khai báo 1 hằng số với giá trị kiểu Number bất kỳ
const number = 15;

// In ra dòng tương ứng dựa trên giá trị của number
if (number > 0) {
    console.log("Giá trị bạn nhập là số dương");
} else if (number < 0) {
    console.log("Giá trị bạn nhập là số âm");
} else {
    console.log("Giá trị bạn nhập là số 0");
}

